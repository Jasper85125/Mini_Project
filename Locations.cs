public class Locations
{
    public int ID;
    public string Name;
    public string Description;
    public var Option_1;
    public var Option_2;

    public Locations(int id, string name, string description, var option_1, var option_2)
    {
        this.ID = id;
        this.Name = name;
        this.Description = Description;
        this.Option_1 = option_1;
        this.Option_2 = option_2;
    }

}